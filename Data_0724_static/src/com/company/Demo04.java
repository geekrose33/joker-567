public class Demo04 {
    public static void main(String[] args) {
        // 绝对值
        System.out.println(Math.abs(-5));

        // 返回大于等于参数的最小整数
        System.out.println(Math.ceil(3.3));
        System.out.println(Math.ceil(-3.3));
        // 返回小于等于参数的最小整数
        System.out.println(Math.floor(-3.3));
        System.out.println(Math.floor(3.3));

        // 四舍五入
        System.out.println(Math.round(5.5));


    }
}
